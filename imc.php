    <h1 style="color: yellow; margin-left: 40px;">¿Qué es?</h1>

    <p style="text-align: justify; margin-left: 40px; margin-right: 40px;">El índice de masa corporal (IMC) es un método utilizado para estimar la cantidad de grasa corporal que tiene una persona, y determinar por tanto si el peso está dentro del rango normal, o por el contrario, se tiene sobrepeso o delgadez. Para ello, se pone en relación la estatura y el peso actual del individuo. Esta fórmula matemática fue ideada por el estadístico belga Adolphe Quetelet, por lo que también se conoce como índice de Quetelet o Body Mass Index (BMI).

Actualmente, esta fórmula está cayendo en desuso porque se está viendo que el IMC no hace diferencia entre la grasa corporal y la muscular, lo que hace que no sea muy exacto. “Un deportista o un culturista van a tener siempre un sobrepeso si tenemos en cuenta su peso respecto a la altura, pero no tienen los problemas de salud que tiene una persona obesa. Esta última tiene problemas debido a la cantidad de grasa que tienen, no por el peso”, explica Carmen Escalada, nutricionista del <b>Instituto Médico Europeo de la Obesidad</b> (IMEO). La cantidad de grasa marca mejor nuestro estado de salud.</p>

<br><br>
<h1 style="color: yellow; margin-left: 40px;">¿Cómo se calcula?</h1>
<p style="text-align: justify; margin-left: 40px; margin-right: 40px;">El IMC es una fórmula que se calcula dividiendo el peso, expresado siempre en Kg, entre la altura, siempre en metros al cuadrado. Una cosa importante que destaca la nutricionista es que no se pueden aplicar los mismos valores en niños y adolescentes que en adultos. “Para calcular el IMC en niños se utilizan los percentiles. Estos son una media en los que se establece el peso del niño y se le relaciona con sus iguales de edad y sexo, dentro de la misma área; y si está en la media, tiene un peso adecuado; si está por encima, habría un percentil alto, por lo que  tendrían obesidad, y si está por debajo, se calificaría como un bajo peso”, indica Escalada.</p>



